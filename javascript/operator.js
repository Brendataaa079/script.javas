//Operator Aritmatematika

console.log(5 + 5); //Penjumlahan // Output: 10
console.log(10 - 5); //Pengurangan // Output: 5
console.log(5 * 5); //Perkalian // Output: 25
console.log(10 / 2); //Pembagian // Output: 5
console.log(10 % 3); //Sisa bagi // Output: 1
console.log(2 ** 3); //Pangkat // Output: 8

//Operator Assignment

let angka = 10;
console.log(angka); // Output: 10
angka += 5; //angka = angka + 5
console.log(angka); // Output: 15

angka -= 3; //angka = angka - 3
console.log(angka); // Output: 12

angka *= 2; //angka = angka * 2
console.log(angka); // Output: 24

angka /= 4; //angka = angka / 4
console.log(angka); // Output: 6

angka %= 5; //angka = angka % 5
console.log(angka); // Output: 1

//Operator Unary

angka++; //angka = angka + 1
console.log(angka); // Output: 2

angka--; //angka = angka - 1
console.log(angka); // Output: 1

//Positif
console.log(+200); // Output: 200
//Negatif
console.log(-200); // Output: -200

//Operator Perbandingan

console.log(5 > 3); //Lebih besar dari // Output: true

console.log(5 < 3); //Lebih kecil dari // Output: false

console.log(5 >= 5); //Lebih besar atau sama dengan // Output: true

console.log(5 <= 5); //Lebih kecil atau sama dengan // Output: true

console.log(5 == 5); //Sama dengan // Output: true

console.log(5 != 3); //Tidak sama dengan // Output: true

console.log(5 === '5'); //Sama dengan dan tipe data sama // Output: false

console.log(5 !== '5'); //Tidak sama dengan atau tipe data tidak sama // Output: true